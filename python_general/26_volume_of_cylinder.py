radius = int(input("Enter the radius of the cylinder (in centimeters) = "))
height = int(input("Enter the height of the cylinder (in centimeters) = "))

# we are taking the value of pi = 3.14
volume_of_cylinder = 3.14 * (radius**2) * height

print(f"The volume of the cylinder is {volume_of_cylinder} cubic centimeters")
